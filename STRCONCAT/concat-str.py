"""
Concatenate Strings: Write a program that takes two strings as input from the user and concatenates them together. For example, if the user enters "Hello" and "World," the program should output "HelloWorld."# Prompt the user for input
"""

# declare a variable string1, and initialise it to  Get the first string from the user using the input function. input function is used to get input from the user
string1 = input("Enter the first string: ")

# declare a variable string2, and initialise it to  Get the first string from the user using the input function.

string2 = input("Enter the second string: ")

# we Concatenate the two strings and save it as concatenated_string
concatenated_string = string1 + string2

# then we Print the concatenated string
print(f"Concatenated string: {concatenated_string}")



