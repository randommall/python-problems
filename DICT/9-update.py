"""
Create a program that updates the value of an existing key in a dictionary.

"""

# Sample dictionary
my_dict = {'name': 'John', 'age': 30, 'city': 'New York'}

# Key to update
key_to_update = 'age'

# New value for the key
new_age = 31

# Update the value of the existing key
my_dict[key_to_update] = new_age

# Print the updated dictionary
print("Updated Dictionary:", my_dict)
